def seed_faq(apps, schema_editor):
    """Создаёт базовые вопросы FAQ, не дублируя существующие."""
    FAQEntry = apps.get_model("planner", "FAQEntry")
    for index, (question, answer) in enumerate(FAQ_ITEMS, start=1):
        FAQEntry.objects.get_or_create(
            question=question,
            defaults={
                "answer": answer,
                "sort_order": index,
                "is_active": True,
            },
        )


def unseed_faq(apps, schema_editor):
    """Удаляет только FAQ, созданные этой миграцией."""
    FAQEntry = apps.get_model("planner", "FAQEntry")
    FAQEntry.objects.filter(question__in=[item[0] for item in FAQ_ITEMS]).delete()


